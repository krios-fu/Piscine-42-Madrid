char	*ft_strlowcase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			if (str[i] != str[i] - 32)
			{
				str[i] = str[i] + 32;
			}
		i++;
	}
	return (str);
}
